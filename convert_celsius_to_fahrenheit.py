# celsius to fahrenheit
celsius = float(input("Enter the temperature in Celsius: "))

to_fahrenheit = ((9/5)*celsius)+32

print(f"{celsius} degree celsius temperature is equal to {to_fahrenheit} degree fahrenheit")



# fahrenheit to celsius
fahrenheit = float(input("Enter the temperature in Fahrenheit: "))

to_celsius = (fahrenheit-32)*(5/9)

print(f"{fahrenheit} degree Fahrenheit temperature is equal to {to_celsius} degree Celsius")